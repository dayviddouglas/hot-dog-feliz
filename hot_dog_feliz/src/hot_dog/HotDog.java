package hot_dog;

public class HotDog {
   private String sabor;
   private int qtd;
   private double preco;
  
   
 
 public String getSabor() {
	return sabor;
 }
 
 public void setSabor(String sabor) {
	this.sabor = sabor;
 }
 
 public int getQtd() {
	return qtd;
 }


 public void setQtd(int qtd) {
	this.qtd = qtd;
 }


public double getPreco() {
	return preco;
}


public void setPreco(double preco) {
	this.preco = preco;
}
 
 
 
 
 
   
}
